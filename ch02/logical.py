print((1 < 2) and ('A' == 'a'))  #前者為真後者為假，and需要兩者皆真材為真
print((-1 < 0) or (-1 > 100))  #前者為真後者為假，or只要一個為真就真
print(not('A' != 'a'))  #'A' != 'a'為真，所以做not運算後結果為假
print(not 2)  #因為2不是0所以為true，做not運算後結果為假
print(2 and 3)  #2不為0是真，經過and運算，傳回第二個運算元3
print(2 or 3)  #2不為0是真，經過and運算，傳回第一個運算元2
print('a' or 'b')  #a不為空字串為真，經過or運算，傳回第一個運算元a
print(0 and 3)  #0是為0是假，經過and運算，傳回第一個運算元0
print('' or 'b')  #空字串為假，經過or運算，傳回第二個運算元b